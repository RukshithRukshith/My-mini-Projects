public class AreaofRectangle
{
    public static void main (String[] args)
    {
        int length = 10;
        int breadth = 5;
        int area;
        area = length * breadth;

        System.out.println("Length is: "+ length);
        System.out.println("Width is: "+ breadth);
        System.out.println("Area of Rectangle is: "+ area);

    }
}