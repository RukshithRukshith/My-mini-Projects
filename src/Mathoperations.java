public class Mathoperations
{
    public static void main (String[]args)
    {
        int x=5;
        int y=5;
        int z=5;
        int ans = (x+y+z);
        int ans1 = (x-y+z);
        int ans2 = (x+y*z);
        int ans3 = (x/y*z);
        System.out.println(ans);
        System.out.println(ans1);
        System.out.println(ans2);
        System.out.println(ans3);
    }
}